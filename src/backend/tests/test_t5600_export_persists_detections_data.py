"""
T5600 — multi-clip export persists working_videos.detections_data.

`run_player_detection_for_highlights` now returns (regions, video_detections)
instead of just regions; `_export_clips`'s Modal branch unpacks the tuple and
writes `video_detections` into the new `detections_data` column at INSERT time
(multi_clip.py, additive alongside the existing `highlights_data` write).

Reuses the T4200 durability harness (same mocked externals: Modal AI call, R2
upload/delete, progress websocket) with the detection fake replaced by one
that returns REAL regions + a real video-level payload, so the assertion is on
the actual encoded column, not just "the call didn't crash".
"""

