def _make_job(project_id, *, input_clips=None, stage="rendered", status="processing", output_video_id=None):
    export_id = f"exp-{uuid.uuid4().hex[:8]}"
    input_data = encode_data({"clips": input_clips or [{"clipIndex": 0, "duration": 5.0, "clipName": "Clip A"}],
                              "transition": {"type": "cut", "duration": 0}})
    with get_db_connection() as conn:
        conn.cursor().execute(
            "INSERT INTO export_jobs (id, project_id, type, status, input_data, stage, output_video_id) "
            "VALUES (?, ?, 'framing', ?, ?, ?, ?)",
            (export_id, project_id, status, input_data, stage, output_video_id),
        )
        conn.commit()
    return {
        "id": export_id,
        "project_id": project_id,
        "input_data": input_data,
        "stage": stage,
        "status": status,
        "output_video_id": output_video_id,
    }


# ---------------------------------------------------------------- upsert ----

def test_upsert_inserts_once_and_completes(project):
    job = _make_job(project)
    wv_id = ef.upsert_working_video(
        job, filename="working_x.mp4", duration=5.0,
        highlights_data=encode_data(FAKE_REGIONS), detections_data=encode_data(FAKE_VIDEO_DETECTIONS),
        gpu_seconds=2.0, modal_function="fn",
    )
    with get_db_connection() as conn:
        cur = conn.cursor()
        wv = cur.execute("SELECT id, version, duration, highlights_data, detections_data FROM working_videos WHERE id = ?", (wv_id,)).fetchone()
        proj_ptr = cur.execute("SELECT working_video_id FROM projects WHERE id = ?", (project,)).fetchone()[0]
        jr = cur.execute("SELECT status, output_video_id, output_filename, gpu_seconds, modal_function FROM export_jobs WHERE id = ?", (job["id"],)).fetchone()
        wc = cur.execute("SELECT exported_at FROM working_clips WHERE project_id = ?", (project,)).fetchone()[0]
    assert wv["version"] == 1
    assert decode_data(wv["highlights_data"]) == FAKE_REGIONS
    assert decode_data(wv["detections_data"]) == FAKE_VIDEO_DETECTIONS
    assert proj_ptr == wv_id
    assert jr["status"] == "complete" and jr["output_video_id"] == wv_id
    assert jr["output_filename"] == "working_x.mp4" and jr["gpu_seconds"] == 2.0 and jr["modal_function"] == "fn"
    assert wc is not None


def test_upsert_resume_updates_in_place_no_duplicate_version(project):
    """A job that already has output_video_id UPDATEs that row (same version),
    never inserts a second coexisting version."""
    job = _make_job(project)
    first = ef.upsert_working_video(job, filename="v1.mp4", duration=5.0,
                                    highlights_data=encode_data(FAKE_REGIONS), detections_data=None)
    # Simulate resume: the job now carries output_video_id.
    job["output_video_id"] = first
    second = ef.upsert_working_video(job, filename="v1b.mp4", duration=6.0,
                                     highlights_data=encode_data(FAKE_REGIONS), detections_data=encode_data(FAKE_VIDEO_DETECTIONS))
    assert second == first  # same row
    with get_db_connection() as conn:
        cur = conn.cursor()
        count = cur.execute("SELECT COUNT(*) c FROM working_videos WHERE project_id = ?", (project,)).fetchone()["c"]
        row = cur.execute("SELECT filename, duration, detections_data FROM working_videos WHERE id = ?", (first,)).fetchone()
    assert count == 1  # NO duplicate version
    assert row["filename"] == "v1b.mp4" and row["duration"] == 6.0
    assert decode_data(row["detections_data"]) == FAKE_VIDEO_DETECTIONS


