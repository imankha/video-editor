def _patch_detect_and_sync(monkeypatch, detect_fn, sync_ok=True):
    monkeypatch.setattr(mc, "run_player_detection_for_highlights", detect_fn)
    monkeypatch.setattr(helpers, "sync_export_db_to_r2", lambda *a, **k: sync_ok)


@pytest.mark.asyncio
async def test_finalize_happy_path(project, monkeypatch):
    job = _make_job(project)

    async def fake_detect(**kwargs):
        return FAKE_REGIONS, FAKE_VIDEO_DETECTIONS

    _patch_detect_and_sync(monkeypatch, fake_detect)
    result = await ef.finalize_export(job, "working_videos/working_x.mp4", TEST_USER_ID, TEST_PROFILE_ID, video_duration=5.0)

    assert result["finalized"] is True and not result.get("sync_failed")
    with get_db_connection() as conn:
        cur = conn.cursor()
        wv = cur.execute("SELECT highlights_data, detections_data, duration FROM working_videos WHERE project_id = ?", (project,)).fetchone()
        jr = cur.execute("SELECT status, stage FROM export_jobs WHERE id = ?", (job["id"],)).fetchone()
    assert decode_data(wv["highlights_data"]) == FAKE_REGIONS
    assert decode_data(wv["detections_data"]) == FAKE_VIDEO_DETECTIONS
    assert wv["duration"] == 5.0
    assert jr["status"] == "complete" and jr["stage"] == "complete"


@pytest.mark.asyncio
async def test_finalize_idempotent_when_already_complete(project, monkeypatch):
    job = _make_job(project, stage="complete", status="complete", output_video_id=777)

